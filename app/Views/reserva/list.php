	function ValidarCamposVaciosReserva(){
		var error = 0;
		if ($('#idreserva').val() == ''){
			Resaltado('idreserva');
			error++;
		}
		if ($('#reservanombre').val() == ''){
			Resaltado('reservanombre');
			error++;
		}
		if ($('#fechainicio').val() == ''){
			Resaltado('fechainicio');
			error++;
		}
		if ($('#fechafin').val() == ''){
			Resaltado('fechafin');
			error++;
		}
		if ($('#tipodoc').val() == ''){
			Resaltado('tipodoc');
			error++;
		}
		if ($('#idpersona').val() == ''){
			Resaltado('idpersona');
			error++;
		}
		if ($('#reservatelefono').val() == ''){
			Resaltado('reservatelefono');
			error++;
		}
		if ($('#reservacorreo').val() == ''){
			Resaltado('reservacorreo');
			error++;
		}
		if ($('#montototal').val() == ''){
			Resaltado('montototal');
			error++;
		}
		if ($('#pagado').val() == ''){
			Resaltado('pagado');
			error++;
		}
		if ($('#estado').val() == ''){
			Resaltado('estado');
			error++;
		}

		return error;
	}


	function Resaltado(id){
		$('#'+id).css('border-color', '#ef5350');
		$('#'+id).focus();
	}


